test_that("hosted and local model adapters share one sanitized prompt contract", {
  result <- autoxplain(mtcars, "mpg", seed = 6)
  result$training_data$mpg[[1L]] <- 987654321
  cases <- list(
    gemini = list(key = "gemini-secret", model = "gemini-test", url = "generateContent"),
    groq = list(key = "groq-secret", model = "groq-test", url = "groq.com"),
    cloudflare = list(
      key = "cloudflare-secret", model = "cloudflare-test",
      url = "accounts/test-account/ai/v1/chat/completions",
      account_id = "test-account"
    ),
    ollama = list(key = NULL, model = "ollama-test", url = "localhost:11434"),
    openrouter = list(key = "router-secret", model = "router-test", url = "openrouter.ai")
  )

  for (provider in names(cases)) {
    captured <- NULL
    details <- cases[[provider]]
    report <- generate_natural_language_report(
      result,
      provider = provider,
      api_key = details$key,
      model = details$model,
      account_id = details$account_id %||% NULL,
      transport = function(request) {
        captured <<- request
        paste("Generated by", provider)
      }
    )
    expect_match(captured$url, details$url, fixed = TRUE)
    expect_equal(captured$model, details$model)
    expect_match(captured$body |> unlist() |> paste(collapse = " "), "Never imply causality")
    expect_false(grepl("987654321", captured$body |> unlist() |> paste(collapse = " "),
                       fixed = TRUE))
    expect_equal(attr(report, "narrative_provenance")$provider_used, provider)
    expect_false(attr(report, "narrative_provenance")$fallback)
  }
})

test_that("custom OpenAI-compatible endpoints and provenance are supported", {
  result <- autoxplain(mtcars, "mpg", seed = 7)
  captured <- NULL
  report <- generate_natural_language_report(
    result,
    provider = "custom",
    model = "local/model",
    base_url = "http://127.0.0.1:9000/v1/chat/completions",
    transport = function(request) {
      captured <<- request
      "Custom narrative"
    }
  )

  expect_equal(captured$response_format, "openai")
  expect_equal(captured$body$model, "local/model")
  expect_equal(attr(report, "narrative_provenance")$model, "local/model")
  expect_false(attr(report, "narrative_provenance")$remote)
})

test_that("provider failures fall back transparently or can be made strict", {
  result <- autoxplain(mtcars, "mpg", seed = 9)
  withr::local_envvar(GEMINI_API_KEY = NA)
  expect_warning(
    report <- generate_natural_language_report(result, provider = "gemini"),
    "requires `api_key`"
  )
  provenance <- attr(report, "narrative_provenance")
  expect_true(provenance$fallback)
  expect_equal(provenance$provider_requested, "gemini")
  expect_equal(provenance$provider_used, "local")
  expect_match(provenance$error, "GEMINI_API_KEY")

  expect_error(
    generate_natural_language_report(
      result, provider = "groq", api_key = "key", fallback = FALSE,
      transport = function(request) stop("rate limited")
    ),
    "rate limited"
  )
  expect_error(
    generate_natural_language_report(
      result, provider = "custom", model = "model", fallback = FALSE
    ),
    "base_url"
  )
})

