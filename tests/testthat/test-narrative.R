test_that("supported providers request one validated narrative schema", {
  result <- autoxplain(mtcars, "mpg", seed = 13)
  response <- paste0(
    '{"headline":"A useful held-out result",',
    '"performance":"RMSE is the typical prediction error on held-out rows.",',
    '"patterns":["Weight was used as a predictive pattern."],',
    '"cautions":["This evaluation sample is finite."],',
    '"next_steps":["Check performance on later data."]}'
  )
  cases <- list(
    gemini = list(key = "gemini-key", model = "gemini-3.5-flash"),
    groq = list(key = "groq-key", model = "openai/gpt-oss-120b"),
    ollama = list(key = NULL, model = "gemma3:4b"),
    openrouter = list(key = "openrouter-key", model = "openrouter/free")
  )

  for (provider in names(cases)) {
    captured <- NULL
    report <- generate_natural_language_report(
      result,
      provider = provider,
      api_key = cases[[provider]]$key,
      model = cases[[provider]]$model,
      transport = function(request) {
        captured <<- request
        response
      }
    )

    expect_true(captured$structured, info = provider)
    schema <- if (provider == "gemini") {
      captured$body$response_format$schema
    } else {
      captured$body$response_format$json_schema$schema
    }
    expect_equal(schema$required, c(
      "headline", "performance", "patterns", "cautions", "next_steps"
    ), info = provider)
    expect_false(schema$additionalProperties, info = provider)
    if (provider == "gemini") {
      expect_false(captured$body$store)
      expect_equal(captured$body$generation_config$temperature, 1)
      expect_equal(captured$body$response_format$mime_type, "application/json")
    }
    if (provider != "gemini") {
      expect_true(captured$body$response_format$json_schema$strict, info = provider)
    }
    if (provider == "openrouter") {
      expect_true(captured$body$provider$require_parameters)
    }
    expect_match(report, "# A useful held-out result", fixed = TRUE)
    expect_match(report, "Required interpretation boundaries", fixed = TRUE)
    expect_match(report, "format, not whether generated statements are true", fixed = TRUE)
    provenance <- attr(report, "narrative_provenance")
    expect_true(provenance$structured_requested, info = provider)
    expect_true(provenance$structured_used, info = provider)
  }
})

test_that("unsupported schemas and invalid provider JSON are transparent", {
  result <- autoxplain(mtcars, "mpg", seed = 14)
  captured <- NULL
  report <- generate_natural_language_report(
    result,
    provider = "cloudflare",
    api_key = "cloudflare-key",
    account_id = "account",
    transport = function(request) {
      captured <<- request
      "A plain Cloudflare response."
    }
  )
  provenance <- attr(report, "narrative_provenance")

  expect_false(captured$structured)
  expect_null(captured$body$response_format)
  expect_true(provenance$structured_requested)
  expect_false(provenance$structured_used)
  expect_match(report, "Required interpretation boundaries", fixed = TRUE)

  expect_warning(
    fallback <- generate_natural_language_report(
      result,
      provider = "gemini",
      api_key = "gemini-key",
      transport = function(request) "{}"
    ),
    "missing"
  )
  expect_true(attr(fallback, "narrative_provenance")$fallback)
  expect_false(attr(fallback, "narrative_provenance")$structured_used)
  expect_error(
    generate_natural_language_report(
      result,
      provider = "gemini",
      api_key = "gemini-key",
      fallback = FALSE,
      transport = function(request) "{}"
    ),
    "missing"
  )
  expect_error(
    generate_natural_language_report(result, structured = NA),
    "structured"
  )
})

test_that("classification and missingness evidence reach the sanitized prompt", {
  set.seed(312)
  training <- data.frame(x = rnorm(120), z = rnorm(120))
  training$accepted <- factor(ifelse(
    stats::plogis(training$x - training$z) > runif(120), "yes", "no"
  ))
  evaluation <- data.frame(x = rnorm(60), z = rnorm(60))
  evaluation$accepted <- factor(ifelse(
    stats::plogis(evaluation$x - evaluation$z) > runif(60), "yes", "no"
  ), levels = levels(training$accepted))
  training$x[seq_len(6)] <- NA
  evaluation$x[seq_len(30)] <- NA
  result <- autoxplain(training, "accepted", test_data = evaluation, seed = 15)
  context <- AutoXplainR:::prepare_analysis_context(result)
  prompt <- AutoXplainR:::context_to_text(context)

  expect_match(prompt, "Probability calibration check", fixed = TRUE)
  expect_match(prompt, "Missingness check: 1 model input", fixed = TRUE)
  expect_match(prompt, "Candidate-selection boundary", fixed = TRUE)
  expect_match(prompt, "Aggregated diagnostics only", fixed = TRUE)
})

test_that("Gemini Interaction responses expose only completed model text", {
  interaction <- list(
    status = "completed",
    steps = list(
      list(type = "thought", content = list()),
      list(
        type = "model_output",
        content = list(
          list(type = "text", text = "first"),
          list(type = "image", data = "ignored"),
          list(type = "text", text = "second")
        )
      )
    )
  )

  expect_equal(
    AutoXplainR:::extract_narrative_text(
      interaction, "gemini_interactions", "gemini"
    ),
    "first\nsecond"
  )
  interaction$status <- "incomplete"
  expect_error(
    AutoXplainR:::extract_narrative_text(
      interaction, "gemini_interactions", "gemini"
    ),
    "incomplete"
  )
})
