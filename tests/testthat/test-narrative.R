  prompt <- AutoXplainR:::create_report_prompt(context)
  expect_match(prompt, "Never imply causality")
  expect_match(prompt, "never certification")
  expect_false(grepl("GEMINI_API_KEY", prompt, fixed = TRUE))
})

test_that("local is the default even when a hosted-provider key exists", {
  withr::local_envvar(GEMINI_API_KEY = "must-not-trigger-a-request")
  result <- autoxplain(mtcars, "mpg", seed = 2)
  called <- FALSE
  report <- generate_natural_language_report(
    result,
    transport = function(request) {
      called <<- TRUE
      "unexpected"
    }
  )

  expect_false(called)
  expect_match(report, "held-out test")
  expect_match(report, "What the main metric means")
  expect_equal(attr(report, "narrative_provenance")$provider_requested, "local")
})

